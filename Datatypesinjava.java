public class Datatypesinjava {
    public static void main(String[] args) {
        int myNum = 5;               // Integer (whole number)    4 Bytes
        float myFloatNum = 5.99f;    // Floating point number     4 Bytes 
        char myLetter = 'D';         // Character                 1 Bytes
        boolean myBool = true;       // Boolean                   1 Bit 
        String myText = "Hello";     // Strin                     

        System.out.println(myText);
        System.out.println(myBool);
        System.out.println(myLetter);
        System.out.println(myFloatNum);
        System.out.println(myNum);
    }
    
}
